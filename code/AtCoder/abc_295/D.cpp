void solve() {
    string s;
    cin >> s;
    vector<bool> state(10, 0);
    int n = s.length();
    map<vector<bool>, int> ct;
    ct[state]=1;
    
    int ans = 0;
    for (int i = 0; i < n; i++) {
    	state[s[i] - '0'] = 1 - state[s[i] - '0'];
    	ans += ct[state]++;
    }
    cout << ans << ln;
}

