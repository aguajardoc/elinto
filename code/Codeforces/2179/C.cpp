void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    int mn = 1e9 + 1;
    for (auto& i : a) {
    	cin >> i;
    	mn =  min(mn, i);
    }
    ll ans = mn;
    ll mnres = 1e9 + 1;
    for (auto& i : a) {
    	if (i == mn) continue;
    	mnres = min(mnres, i - mn);
    }
    
    ans = max(mn, mnres);
    cout << ans << ln;
}

